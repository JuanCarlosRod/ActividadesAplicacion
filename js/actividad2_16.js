// Desarrolla un programa con la logica que desees donde expliques las diferencias de usar los operadores
// a++ , a--, ++a, --a. Utiliza los comentarios de 1 y varias lineas en tu explicacion.
// Ejemplo de operadores de incremento y decremento en JavaScript

let a = 5;

let resultadoIncreDesp = a++;
console.log('resultadoIncreDesp:', resultadoIncreDesp);
console.log('a después de a++:', a);

let resultadoDecreDesp = a--;
console.log('resultadoDecreDesp:', resultadoDecreDesp);
console.log('a después de a--:', a);

let resultadoIncreAnt = ++a;
console.log('resultadoIncreAnt:', resultadoIncreAnt);
console.log('a después de ++a:', a);

let resultadoDecreAnt = --a;
console.log('resultadoDecreAnt:', resultadoDecreAnt);
console.log('a después de --a:', a);

